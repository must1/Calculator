public class Calculations {

    private int result;

    int add(int a, int b) {
        result = a + b;
        return result;
    }

    int substract(int a, int b) {
        result = a - b;
        return result;
    }

    int multiply(int a, int b) {
        result = a * b;
        return result;
    }

    int divide(int a, int b) {
        result = a / b;
        return result;
    }

}
